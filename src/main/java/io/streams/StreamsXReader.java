package io.streams;

public class StreamsXReader {

	/**
	 * Those classes that have 'Stream' in the name are for Reading/Writing bytes in
	 * a File. While classes that have Reader/Writer in the name, are for
	 * Reading/Writing strings/characters in the File.
	 */
	
	/**
	 * 1. A class with InputStream or OutputStream is used to read/write binary data.
	 * 2. A class with Reader or Writer is used to read/write character or string data.
	 * 3. Most, but not all, input classes have a corresponding output class.
	 * 4. A low-level stream connects directly with the source of the data.
	 * 5. A high-level stream wraps the low-level stream giving more utils methods.
	 * 6. A class with Buffered in its name reads/writes data in groups of bytes and improves the performance of it. (high-level stream)
	 */

}
